/**
 * Calculates average between 3 scores
 * Created by cobalt on 9/21/15.
 */
public class Average {
    public static void main(String [] args){

        // Initialization and assignment of values
        double firstScore = 80;
        double secondScore = 90;
        double thirdScore = 100;

        // Compute the average
        double average = (firstScore + secondScore + thirdScore)/3;

        // Output the result
        System.out.println("The average of 3 scores is " + average);
    }
}
